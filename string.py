# slice operator
# here we are writing full string 
a='string'
print(a[0:])

# here we write only one single character from a string
print(a[0])
print(a[1])
print(a[2])
print(a[3])
print(a[4])
print(a[5])

# here we write from particular index to particular index
print('anand')
print(a[2:4])
print('other statement')
print(a[0:5])


