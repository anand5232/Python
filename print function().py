# we can use double quotes to print the string
print("hello")

# we can also use single quotes to print the string
print('my name is anand')

# using escape sequence character inside the print() function
print(" this is my first line \nthis is my second line ")

# to escape the escape  sequence we can use single \ before escape sequence
print(" this is my first line \\n this is the second line ")

# to repeat the string we can use *
print(3*'anand')

# to add the two string we can use + operator here we are going to add two string
print(" city name="+"hydrabad")



